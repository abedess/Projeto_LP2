package ECO.PESSOA;

/**
 * Esta classe define uma interface de Funcao.
 */
public interface Funcao {
	
	/**
	 * Este atributo representa o toString de funcao.
	 * @return retorna a representacao textual, do tipo String.
	 */
    String toString();

	/**
	 * Metodo para a incrementar o contador de Leis de Deputado.
	 */
    void criaLei();
}